let cachedMembers: MemberRecord[] | null = null;

export function generateSeedMembers(): MemberRecord[] {
  if (cachedMembers) return cachedMembers;
  const members: MemberRecord[] = [];
  for (let i = 1; i <= 4000; i++) {
    const firstName = firstNames[(i - 1) % 50];
    const lastName = lastNames[Math.floor((i - 1) / 50)];
    members.push({
      id: i,
      name: `${firstName} ${lastName}`,
      email: `${firstName.toLowerCase()}.${lastName.toLowerCase()}${i}@earningbysurfing.com`,
      gender: i % 3 === 0 ? "Female" : "Male",
      commission: 5 + ((i * 7) % 11),
      status: i % 2 === 0 ? "Paid" : "Pending",
    });
  }
  cachedMembers = members;
  return members;
}
